import { beep, beepMany } from "./beep";

const sounds = {
  onDrop: () => {
    beepMany(
      [30, 70, 8, "sawtooth"],
      [30, 60, 8, "sine"],
      [30, 80, 8, "square"],
      [30, 70, 5, "sawtooth"],
      [30, 80, 3, "sine"]
    );
  },
  onTetris: () => {
    beepMany(
      [100, 600, 10],
      [50, 720, 10],
      [100, 600, 10],
      [50, 720, 10],
      [100, 600, 10],
      [100, 720, 5]
    );
  },
  onClear: () => {
    beepMany([100, 500, 10], [50, 600, 10], [100, 500, 5]);
  },
  onLevelUp: () => {
    beepMany(
      [50, 300, 10],
      [100, 500, 10],
      [50, 0, 0],
      [50, 400, 10],
      [100, 600, 10],
      [50, 0, 0],
      [50, 500, 10],
      [100, 700, 10]
    );
  },
  onFinal: () => {
    beepMany(
      [250, 720, 10],
      [100, 0, 0],
      [250, 600, 10],
      [100, 0, 0],
      [1000, 510, 10]
    );
  },
  onMove: () => {
    beep(50, 720, 10);
  },
  onRotate: () => {
    beepMany([50, 600, 10], [50, 0, 0], [50, 600, 10]);
  },
};

export default sounds;
